import { useState } from "react";
import { useUser } from "../hoocks/userContext";
import Router from 'next/router'
import Head from 'next/head'
import Link from 'next/link'

const URL = process.env.NEXT_PUBLIC_APP_URL;

const SignIn = ()=>{
  const [text, setText] = useState('');
  const [msgError, setMsgError] = useState("");
  const [pass, setPass] = useState('');
  const {login} = useUser();

  const handleSubmit = async(event)=>{
    console.log("========inicio signIn handleSubmit==========")
    console.log("Clicado em Login");
    event.preventDefault();
    setMsgError('');
    if(text==='' || pass===''){
      console.log("Preencha todos os campos")
      console.log("========fim signIn handleSubmit==========")
      setMsgError('*Preencha todos os campos');
      return;
    }
    console.log("acesso a API");
    const response = await login({
      text: text,
      pass: pass,
    });
    console.log("resposta a ter usuario:")
    console.log(response);
    if(response.status){
      console.log("usuario existente");
      console.log("redirecionando para home")
      console.log("========fim signIn handleSubmit==========")
      Router.push('/');
    }else{
      console.log("usuario inexistente");
      console.log("========fim signIn handleSubmit==========")
      setMsgError(`ERRO: ${response.msg}`);
    }
    setText('');
    setPass('');
  }
  return(
    <div style={{
      display: 'flex', 
      flexDirection: 'column', 
      alignItems: 'center', 
      justifyContent: 'center',
      padding: '50px',
      backgroundColor: '#f71',
      height: '100vh',
    }}>
      <Head>
        <title>Login</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <form onSubmit={handleSubmit} style={{
        alignSelf: 'center',
        alignItems: 'center',
        justifyContent: 'space-between',
        padding: '0px 50px',
        backgroundColor: 'aliceblue',
        borderRadius: '20px',
        width: '40vw',
      }}>
        <div style={{
          alignItems: 'flex-start',
          display: 'flex',
          flexDirection: 'column',
        }}>
          <h1 style={{alignSelf: 'center'}}>Login</h1>
          <h3 style={{marginBottom: '5px'}}>E-mail ou Username:</h3>
          <input 
            type="text" 
            placeholder="Nome ou Email"
            value={text}
            onChange={(e)=>setText(e.target.value)} 
            style={{
              width: '100%',
              borderRadius: '20px',
              padding: '5px 20px',
              boxShadow: '0 0 0 0',
              borderWidth: '2px',
              borderColor: '#f71',
              outline: 0,
            }}
          />
          <h3 style={{marginBottom: '5px'}}>Senha:</h3>
          <input 
            type="password" 
            placeholder="Senha" 
            value={pass}
            onChange={(e)=>setPass(e.target.value)} 
            style={{
              width: '100%',
              borderRadius: '20px',
              padding: '5px 20px',
              boxShadow: '0 0 0 0',
              borderWidth: '2px',
              borderColor: '#f71',
              outline: 0,
            }}
          /><br/>
        </div>
        <div style={{display: 'flex', flexDirection: 'column', alignItems: 'center'}}>
          <input 
            type="submit" 
            value="Login"
            style={{
              marginTop: '20px', 
              borderRadius: '20px', 
              padding: '10px 20px',
              boxShadow: '0 0 0 0',
              borderWidth: '2px',
              borderColor: '#f71',
              outline: 0,
              backgroundColor: 'aliceblue',   
              width: '100%',       
            }}
          />
          <p style={{cursor: 'default'}}>NÃ£o tem uma conta? Clique  <Link href="/signUp"><strong><a style={{cursor: 'pointer'}}>aqui</a></strong></Link> para se registrar</p>
          <p style={{color: '#f00', textAlign: 'center', margin: '5px 0px', fontSize: '12px', visibility: msgError===''&&'hidden'}}>{msgError===''?'.':msgError}</p> 
        </div>
      </form>
    </div>
  );
}
